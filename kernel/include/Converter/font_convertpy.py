def to_c_array(binary_lines):
    return '{ ' + ', '.join(f'0b{line}' for line in binary_lines) + ' }'

def generate_c_fonts(text):
    text = text.upper()
    generated = []
    for char in text:
        if char not in font5x5:
            print(f"Warning: Character '{char}' not supported, replaced with space.")
            char = ' '
        array = to_c_array(font5x5[char])
        generated.append(f"uint8_t font_{char}[5] = {array};")
    return '\n'.join(generated)

